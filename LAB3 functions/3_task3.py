def count_letters(text):
    """
    Подсчитывает количество каждой буквы в тексте.

    :param text: Входной текст
    :return: Словарь с количеством каждой буквы в нижнем регистре
    """
    letter_count = {}

    for char in text:
        if char.isalpha():  # Проверяем, является ли символ буквой
            char_lower = char.lower()  # Приводим к нижнему регистру
            if char_lower in letter_count:
                letter_count[char_lower] += 1
            else:
                letter_count[char_lower] = 1

    return letter_count


def calculate_frequency(letter_count):
    """
    Вычисляет частоту каждой буквы.

    :param letter_count: Словарь с количеством каждой буквы
    :return: Словарь с частотой каждой буквы
    """
    total_letters = sum(letter_count.values())  # Общее количество букв
    frequency = {}

    for letter, count in letter_count.items():
        frequency[letter] = round(count / total_letters, 2)  # Вычисляем частоту и округляем до 2 знаков

    return frequency


