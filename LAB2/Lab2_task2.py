# Исходные данные
salary = 5000  # Ежемесячная зарплата
spend = 6000   # Траты за первый месяц
months = 10    # Количество месяцев, которое планируется протянуть без долгов
increase = 0.05  # Ежемесячный рост расходов (5%)

# Инициализация переменной для расчета общей подушки безопасности
total_needed = 0

# Цикл для расчета расходов на каждый месяц
current_spend = spend  # Начальные расходы
for month in range(1, months + 1):
    # Определяем нехватку средств
    deficit = current_spend - salary

    # Если есть нехватка, добавляем ее к общей необходимости
    if deficit > 0:
        total_needed += deficit

    # Увеличиваем расходы на 5% для следующего месяца
    current_spend *= (1 + increase)

# Округление до целого числа
total_needed = round(total_needed)

# Вывод результата
print(f"Подушка безопасности, чтобы протянуть {months} месяцев без долгов: {total_needed} ")
