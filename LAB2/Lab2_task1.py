# Исходные данные
money_capital = 20000  # Финансовая подушка безопасности
salary = 5000           # Ежемесячная зарплата
spend = 6000            # Траты за первый месяц
increase = 0.05         # Ежемесячный рост расходов (5%)
months = 0              # Счетчик месяцев

# Цикл для расчета, сколько месяцев можно протянуть
current_spend = spend  # Начальные расходы
while True:
    # Бюджет текущего месяца
    budget = salary + money_capital

    # Проверяем, хватает ли бюджета на текущие расходы
    if budget >= current_spend:
        # Уменьшаем подушку безопасности на нехватку средств
        money_capital -= (current_spend - salary)
        months += 1  # Увеличиваем счетчик месяцев
    else:
        break  # Если бюджета не хватает, выходим из цикла

    # Увеличиваем расходы на 5% для следующего месяца
    current_spend *= (1 + increase)

# Вывод результата
print(f"Количество месяцев, которое можно протянуть без долгов: {months} ")